export const DEFAULT_TEAM_SLUG = 'thunder';

export const getTeamBySlug = (slug = DEFAULT_TEAM_SLUG) => {
  if (!slug) return undefined;
  return NBA_TEAMS.find((team) => team.slug === slug.toLowerCase());
};

export const getTeamById = (teamId) => {
  if (!teamId) return undefined;
  return NBA_TEAMS.find((team) => team.id === String(teamId));
};
